public class fifteenth {
    public static void main(String[] args) {
        int n =5;
        int s =1;
        for(int i =1;i<=n;i++){
            for (int j = 1; j <=i; j++) {
                System.out.print(s+" ");
                s++;
            }
            System.out.print("\n");
        }
    }
}
